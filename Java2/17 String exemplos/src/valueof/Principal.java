package valueof;

public class Principal {

	public static void main(String[] args) {

		
		//Criando uma variavel do tipo String
		String quantidade = "";
		
		//Criando uma variavel do tipo int e atribuindo o valor 5
		int numero = 5;
		
		//Atribuindo e mostrando o valro da variavel int na variavel String
		System.out.println(quantidade.valueOf(numero));
	}

}
