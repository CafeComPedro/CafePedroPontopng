package max_min;

public class Principal {
	public static void main(String[] args) {

		//Array de números
		double numeros[] = {1, 15};
		
		//Exibir o mairo e o menor numero
		System.out.println("O maior numero é "+ Math.max(numeros[0], numeros[1]));
		System.out.println("O menor numero é "+ Math.min(numeros[0], numeros[1]));

	}
}
